# Pengertian Tipe Data Tuple Python

# Tipe data tuple tidak bisa dirubah setelah di deklarasikan

foo = ("Belajar", "Python", "di", "Duniailkom")
bar = (100, 200, 300, 400)
baz = ("Python", 200, 6.99, True)
  
print(foo)
print(bar)
print(baz)

# Cara Mengakses Tipe Data Tuple Python

foo = ("Python", 200, 6.99, True, 'Duniailkom', 99j)
  
print(foo[0])
print(foo[1])
print(foo[2])
print(foo[2:5])
print(foo[:3])
print(foo[3:])
print(foo[:])
